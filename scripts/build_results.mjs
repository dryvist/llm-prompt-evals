#!/usr/bin/env node
// Assemble a GitHub-viewable results report (results/README.md) from the JSON
// outputs of an eval run. Pure formatting — no API calls, no cost. Part of the
// standard flow (see scripts/report.sh): run eval -> grade prompts -> cost
// report -> build_results -> commit results/.
//
// Usage: node scripts/build_results.mjs \
//   --main output/graderA.json --graderB output/graderB.json \
//   --fable output/fable.json --quality output/prompt_quality.json \
//   --cost output/cost.json --out results/README.md

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname } from "node:path";

const args = Object.fromEntries(
  process.argv.slice(2).join(" ").split("--").filter(Boolean)
    .map((s) => s.trim().split(/\s+/)).map(([k, ...v]) => [k, v.join(" ")]),
);
const load = (p) => { try { return JSON.parse(readFileSync(p, "utf8")); } catch { return null; } };
const OUT = args.out || "results/README.md";

const main = load(args.main || "output/graderA.json");
const graderB = load(args.graderB || "output/graderB.json");
const fable = load(args.fable || "output/fable.json");
const hard = args.hard ? load(args.hard) : null;
const quality = load(args.quality || "output/prompt_quality.json");
const cost = load(args.cost || "output/cost.json");

const R = (d) => (d && d.results && d.results.results) || [];
const sut = (r) => (r.provider && (r.provider.label || r.provider.id)) || "?";

function scoreboard(results) {
  const P = [...new Set(results.map((r) => r.prompt.label))];
  const S = [...new Set(results.map(sut))];
  const T = [...new Set(results.map((r) => r.testCase && r.testCase.description).filter(Boolean))];
  const sc = {};
  for (const r of results) { const k = sut(r) + "||" + r.prompt.label; sc[k] = (sc[k] || 0) + (r.success ? 1 : 0); }
  let md = `| prompt \\ runner | ${S.join(" | ")} |\n| --- | ${S.map(() => "---:").join(" | ")} |\n`;
  // rank prompts by total across SUTs
  const tot = (p) => S.reduce((a, s) => a + (sc[s + "||" + p] || 0), 0);
  for (const p of P.sort((a, b) => tot(b) - tot(a))) {
    md += `| ${p} | ${S.map((s) => `${sc[s + "||" + p] || 0}/${T.length}`).join(" | ")} |\n`;
  }
  return md;
}

// grader disagreements
function disagreements() {
  if (!main || !graderB) return null;
  const key = (r) => sut(r) + " | " + r.prompt.label + " | " + (r.testCase.description || "");
  const mb = {}; for (const r of R(graderB)) mb[key(r)] = r.success;
  const rows = [];
  for (const r of R(main)) { const k = key(r); if (k in mb && r.success !== mb[k]) rows.push(k); }
  return rows;
}

let md = `# Hermes prompt-eval — results\n\n`;
md += `_Auto-generated by \`scripts/build_results.mjs\`. Renders directly in GitHub._\n\n`;

md += `## What this is\n\nOne base system prompt for a Cribl/Splunk/OCSF log-monitoring expert ("Hermes"), rewritten by many AI models, then scored on the same tests across several runner models. Two questions are answered separately:\n\n1. **Output eval** — which prompt produces the best *answers*? (below)\n2. **Prompt-quality meta-eval** — which prompt is best *engineered*? (further below)\n\n`;

if (main) {
  md += `## Output eval — pass counts (prompt × runner)\n\n`;
  md += scoreboard(R(main));
  md += `\nEach cell is passes out of the full test set on that runner. Runner held constant per column so the score isolates the prompt.\n\n`;
}
if (fable) {
  md += `### Premium-runner pass: Claude Fable 5 (curated subset)\n\n`;
  md += scoreboard(R(fable));
  md += `\n`;
}

if (hard) {
  const HR = R(hard);
  const strongest = "opus-4.8-SUT";
  const T = [...new Set(HR.map((r) => r.testCase.description))];
  const P = [...new Set(HR.map((r) => r.prompt.label))];
  const sc = {}; for (const r of HR) { const k = sut(r) + "||" + r.prompt.label; sc[k] = (sc[k] || 0) + (r.success ? 1 : 0); }
  const opusVals = P.map((p) => sc[strongest + "||" + p] || 0);
  const lo = Math.min(...opusVals), hi = Math.max(...opusVals);
  const perTest = T.map((t) => {
    const c = P.filter((p) => { const r = HR.find((x) => sut(x) === strongest && x.prompt.label === p && x.testCase.description === t); return r && r.success; }).length;
    return { t, c, n: P.length };
  }).sort((a, b) => a.c - b.c);
  md += `## Hard tier — headroom check\n\n`;
  md += `A good eval must NOT be maxable by frontier models, or it cannot discriminate. This tier is designed to leave headroom on the strongest runner.\n\n`;
  md += scoreboard(HR);
  md += `\n**Strongest runner (Opus 4.8) range across prompts: ${lo}–${hi}/${T.length}** — headroom exists (not a flat max), so the tier discriminates.\n\n`;
  md += `Which hard items kept headroom on Opus (low pass-count = still challenging; high = replace with something harder):\n\n`;
  md += `| pass on Opus | hard item |\n| ---: | --- |\n`;
  for (const x of perTest) md += `| ${x.c}/${x.n} | ${x.t.replace(/^HARD[^:]*:\s*/, "")} |\n`;
  md += `\n> Meta-lesson: designing items hard enough to challenge a frontier model is itself iterative — here only ${perTest.filter((x) => x.c < x.n).length}/${T.length} items kept headroom on Opus; the rest need hardening.\n\n`;
}

md += `## Key finding\n\n> **The value of a good system prompt is inversely proportional to how strong the runner is.** On top-tier runners (Opus 4.8, Fable 5) even the bare \`base\` prompt scores near-perfect — the model compensates for a weak prompt. On cheaper/weaker runners (DeepSeek Flash) the disciplined prompts pull real weight. Evaluate on the model you will actually deploy.\n\n`;

const dis = disagreements();
if (dis) {
  md += `## Dual-grader validation\n\nEvery AI-judge verdict was graded by two independent models (Haiku 4.5 and Gemini 3 Flash). Disagreements flag where a rubric is ambiguous — a human then adjudicates.\n\n`;
  md += `- Grader disagreements: **${dis.length}** cell(s).\n`;
  for (const d of dis) md += `  - ${d}\n`;
  md += `\n`;
}

if (quality) {
  md += `## Prompt-quality meta-eval (grades the prompt text itself)\n\n`;
  md += `Judges: ${quality.judges.join(", ")}. Each prompt scored 0–10 on role clarity, scope, anti-fabrication, output formatting, Opus-4.8 best practices, and dual-mode behavior.\n\n`;
  md += `| prompt | overall | words | judge spread |\n| --- | ---: | ---: | ---: |\n`;
  for (const r of quality.rows) md += `| ${r.name} | ${r.overall?.toFixed(1) ?? "?"} | ${r.words} | ${r.spread >= 3 ? "⚠️" + r.spread : r.spread} |\n`;
  md += `\n⚠️ = judges disagreed by ≥3 (rubric/prompt ambiguous).\n\n`;
}

if (cost) {
  md += `## Cost & subscription report\n\n`;
  md += `| model | cells | prompt tok | completion tok | metered $ | subscription |\n| --- | ---: | ---: | ---: | ---: | --- |\n`;
  for (const r of cost.rows) {
    const ex = r.extrapolated ? "*" : "";
    md += `| ${r.id} | ${r.cells} | ${r.prompt.toLocaleString()}${ex} | ${r.completion.toLocaleString()}${ex} | $${r.cost.toFixed(r.cost < 1 ? 4 : 2)} | ${r.plan || "— (OpenRouter only)"} |\n`;
  }
  const eff = cost.totalMetered - cost.totalSubEligible;
  md += `\n**Total metered (OpenRouter): $${cost.totalMetered.toFixed(2)}.** Subscription-eligible: $${cost.totalSubEligible.toFixed(2)} → **effective out-of-pocket ≈ $${eff.toFixed(2)}** if routed via Claude/Codex/Agy subscriptions. \\* = extrapolated from live cells (partly cached); \`--no-cache\` for exact.\n\n`;
}

md += `## Reproduce\n\n\`\`\`bash\nnix develop\ndoppler run -p ai-ci-automation -c prd -- ./scripts/report.sh evals/demo-logmon/promptfooconfig.yaml\n\`\`\`\n`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, md);
console.log(`wrote ${OUT} (${md.length} bytes)`);
